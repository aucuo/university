module AddFunctions

// Функция сложения трех чисел типа int
let addInt (a: int) (b: int) (c: int): int = a + b + c

// Функция сложения трех чисел типа float
let addFloat (a: float) (b: float) (c: float): float = a + b + c

// Функция конкатенации трех строк
let addString (a: string) (b: string) (c: string): string = a + b + c
